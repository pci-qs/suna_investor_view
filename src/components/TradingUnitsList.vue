      this.items_period[2].all_trades = tmp_tradingUnits.length;
      let total_volume = 0;
      let total_dur = 0;
      let long_dur = 0;
      let short_dur = 0;
      tmp_tradingUnits.map((tmp) => {
        // counting during time
        if (tmp.closeTime < "1000") {
          tmp.closeTime = new Date(
            Date.now() - new Date().getTimezoneOffset() * 60000
          ).toISOString();
        }
        let dur_time =
          new Date(tmp.closeTime).getTime() - new Date(tmp.openTime).getTime();
        total_dur += dur_time;
        //// counting volume and trades
        total_volume += tmp.volume;
        this.items_period[0].all_trades = (
          Number(this.items_period[0].all_trades) + Number(tmp.netProfit)
        ).toFixed(2);
        this.items_period[1].all_trades = (
          Number(this.items_period[1].all_trades) + Number(tmp.commission)
        ).toFixed(2);
        if (tmp.isBuy) {
          this.items_period[0].long_trades = (
            Number(this.items_period[0].long_trades) + Number(tmp.netProfit)
          ).toFixed(2);
          this.items_period[1].long_trades = (
            Number(this.items_period[1].long_trades) + Number(tmp.commission)
          ).toFixed(2);
          long_dur += dur_time;
          this.items_period[2].long_trades++;
          if (tmp.netProfit > 0) {
            this.items_period[3].long_trades++;
          } else this.items_period[5].long_trades++;
          if (tmp.netProfit > this.items_period[4].long_trades) {
            this.items_period[4].long_trades = Number(tmp.netProfit).toFixed(2);
          }
          if (tmp.netProfit < this.items_period[6].long_trades) {
            this.items_period[6].long_trades = Number(tmp.netProfit).toFixed(2);
          }
          this.items_period[9].long_trades += Number(tmp.volume);
        } else {
          this.items_period[0].short_trades = (
            Number(this.items_period[0].short_trades) + Number(tmp.netProfit)
          ).toFixed(2);
          this.items_period[1].short_trades = (
            Number(this.items_period[1].short_trades) + Number(tmp.commission)
          ).toFixed(2);
          short_dur += dur_time;
          this.items_period[2].short_trades++;
          if (tmp.netProfit > 0) {
            this.items_period[3].short_trades++;
          } else this.items_period[5].short_trades++;
          if (tmp.netProfit > this.items_period[4].short_trades) {
            this.items_period[4].short_trades = Number(tmp.netProfit).toFixed(
              2
            );
          }
          if (tmp.netProfit < this.items_period[6].short_trades) {
            this.items_period[6].short_trades = Number(tmp.netProfit).toFixed(
              2
            );
          }
          this.items_period[9].short_trades += Number(tmp.volume);
        }
        if (tmp.netProfit > 0) {
          this.items_period[3].all_trades++;
        } else {
          this.items_period[5].all_trades++;
        }
        if (tmp.netProfit > this.items_period[4].all_trades) {
          this.items_period[4].all_trades = Number(tmp.netProfit).toFixed(2);
        }
        if (tmp.netProfit < this.items_period[6].all_trades) {
          this.items_period[6].all_trades = Number(tmp.netProfit).toFixed(2);
        }
        this.items_period[9].all_trades += Number(tmp.volume);

        ////// Starting setting items_symbol
        this.items_symbol.map((sym, i) => {
          if (tmp.symbolName == sym.symbol) {
            sym.volume += tmp.volume;
            sym.trades++;
            if (tmp.netProfit > 0) sym.winning++;
            else sym.losing++;
            sym.netProfit = (
              Number(sym.netProfit) + Number(tmp.netProfit)
            ).toFixed(2);
            this.items_symbol[i] = sym;
          }
        });
      });
      this.items_period[7].all_trades =
        this.items_period[2].all_trades == 0
          ? 0
          : (
              this.items_period[0].all_trades / this.items_period[2].all_trades
            ).toFixed(2);
      this.items_period[7].long_trades =
        this.items_period[2].long_trades == 0
          ? 0
          : (
              this.items_period[0].long_trades /
              this.items_period[2].long_trades
            ).toFixed(2);
      this.items_period[7].short_trades =
        this.items_period[2].short_trades == 0
          ? 0
          : (
              this.items_period[0].short_trades /
              this.items_period[2].short_trades
            ).toFixed(2);
      total_dur =
        this.items_period[2].all_trades == 0
          ? 0
          : (total_dur / this.items_period[2].all_trades).toFixed();
      long_dur =
        this.items_period[2].long_trades == 0
          ? 0
          : (long_dur / this.items_period[2].long_trades).toFixed();
      short_dur =
        this.items_period[2].short_trades == 0
          ? 0
          : (short_dur / this.items_period[2].short_trades).toFixed();
      this.items_period[8].all_trades = this.calculating_during(total_dur);
      this.items_period[8].long_trades = this.calculating_during(long_dur);
      this.items_period[8].short_trades = this.calculating_during(short_dur);
      this.items_symbol.map((sym, index) => {
        sym.volume_per =
          total_volume == 0
            ? 0
            : ((sym.volume / total_volume) * 100).toFixed(2) + "%";
        sym.winning_per =
          sym.winning == 0
            ? 0
            : ((sym.winning / sym.trades) * 100).toFixed() + "%";
        sym.losing_per =
          sym.losing == 0
            ? 0
            : ((sym.losing / sym.trades) * 100).toFixed() + "%";
        sym.avg_trade_size =
          sym.trades == 0
            ? "USD 0"
            : "USD " + (sym.volume / sym.trades).toFixed(2);
        sym.volume = "USD " + sym.volume;
        this.items_symbol[index] = sym;

        this.series5[index].data.push(sym.trades);
      });
    },
  },
  async mounted() {
    this.retrieveIDs();
    this.sel_chart1_period("all");
    this.sel_chart2_period("all");
    this.sel_chart3_period("2021");
    this.sel_chart4_period("month");
    this.sel_breakdown("trades");
    this.sel_history("history");
    this.change_period("cur_month");
  },
};
