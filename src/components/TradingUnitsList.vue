      let dates = this.tradingUnits.map((u) => new Date(u.openTime));
      let netProfits = this.tradingUnits.map(
        (u) => Number(u.grossProfit) + 2 * Number(u.commission) + Number(u.swap)
      );
      let minDate = new Date(Math.min(...dates));
      let maxDate = new Date(Math.max(...dates));
      minDate.setMonth(minDate.getMonth(), 1);
      this.monthlynetProfits = [];
      this.monthlynetProfits[0] = ["Monat", "netProfit"];
      let iMonth = 0;
      for (
        let date = minDate;
        date <= maxDate;
        date.setMonth(date.getMonth() + 1)
      ) {
        let nextMonth = new Date(date.getFullYear(), date.getMonth() + 1, 1);
        let sum = 0;
        for (let i = 0; i < dates.length; i++) {
          if (dates[i] >= date && dates[i] < nextMonth) {
            sum += netProfits[i];
          }
        }
        iMonth++;
        var dateString = "" + (date.getMonth() + 1) + "." + date.getFullYear();
        this.monthlynetProfits[iMonth] = [dateString, sum];
      }
      this.monthlynetProfits[iMonth + 1] = ["8.2021", 20];
